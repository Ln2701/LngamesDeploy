function setupNavigation() {
    // Keyboard listener for slides navigation
    document.addEventListener("keydown", (e) => {
        if (e.key === "ArrowRight" || e.key === "Right" || e.key === "Spacebar" || e.key === " ") {
            // Prevent spacebar scrolling default behavior
            if (e.key === " " || e.key === "Spacebar") e.preventDefault();
            nextSlide();
        } else if (e.key === "ArrowLeft" || e.key === "Left") {
            prevSlide();
        }
    });

    // Touch Swipe Support for Tablets/Phones
    let touchStartX = 0;
    let touchEndX = 0;
    const slideDeck = document.getElementById("slideDeck");
    
    slideDeck.addEventListener("touchstart", (e) => {
        touchStartX = e.changedTouches[0].screenX;
    }, { passive: true });
    
    slideDeck.addEventListener("touchend", (e) => {
        touchEndX = e.changedTouches[0].screenX;
        handleSwipe();
    }, { passive: true });
    
    function handleSwipe() {
        if (touchStartX - touchEndX > 50) {
            nextSlide(); // Swipe left -> next slide
        }
        if (touchEndX - touchStartX > 50) {
            prevSlide(); // Swipe right -> prev slide
        }
    }
}

function updateSlideUI() {
    // Hide all slides and remove animation classes
    document.querySelectorAll(".slide").forEach((slide) => {
        slide.classList.remove("active", "exit-left", "enter-right");
    });

    // Show active slide
    const activeSlide = document.getElementById(`slide-${currentSlide}`);
    if (activeSlide) {
        activeSlide.classList.add("active");
    }

    // Update progress bar
    const progressPercent = ((currentSlide - 1) / (totalSlides - 1)) * 100;
    document.getElementById("progressBar").style.width = `${progressPercent}%`;

    // Update slide counter
    document.getElementById("currentSlideNum").textContent = currentSlide;

    // Update arrow button disabled states
    document.getElementById("btnPrev").classList.toggle("disabled", currentSlide === 1);
    document.getElementById("btnNext").classList.toggle("disabled", currentSlide === totalSlides);
    
    // Update active TOC item
    document.querySelectorAll(".toc-item").forEach((item, index) => {
        item.classList.toggle("active", index + 1 === currentSlide);
    });

    // Update speaker notes drawer
    if (typeof updateSpeakerNotesUI === 'function') {
        updateSpeakerNotesUI();
    }
}

function goToSlide(slideNum) {
    if (slideNum < 1 || slideNum > totalSlides) return;
    
    playSynthesizedSound('slide');
    
    const prevSlideEl = document.getElementById(`slide-${currentSlide}`);
    
    // Set exit animation
    if (prevSlideEl) {
        if (slideNum > currentSlide) {
            prevSlideEl.classList.add("exit-left");
        } else {
            prevSlideEl.classList.add("enter-right");
        }
    }

    currentSlide = slideNum;
    
    // Delay slightly to let exit transitions play if applicable
    setTimeout(() => {
        updateSlideUI();
    }, 50);

    // Close TOC Drawer if it was open
    closeTOC();
}

function nextSlide() {
    if (currentSlide < totalSlides) {
        goToSlide(currentSlide + 1);
    }
}

function prevSlide() {
    if (currentSlide > 1) {
        goToSlide(currentSlide - 1);
    }
}

