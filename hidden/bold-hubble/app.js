/* ==========================================================================
   WEB AUDIO MAIN MAIN MAIN CONTINUOUS DRONE MAIN FRAME MAIN SERVER HUM
   ========================================================================== */
let droneOsc = null;
let droneGain = null;

function startAmbientHum() {
    if (isSoundMuted) return;
    try {
        initAudioContext();
        if (audioCtx.state === 'suspended') {
            audioCtx.resume();
        }
        
        if (droneOsc) return; // Already humming
        
        droneOsc = audioCtx.createOscillator();
        droneGain = audioCtx.createGain();
        
        droneOsc.type = 'sine';
        droneOsc.frequency.setValueAtTime(55, audioCtx.currentTime); // Deep A1 55Hz mainframe sine wave hum
        
        // Exquisite background hum (very low volume to avoid annoyance)
        droneGain.gain.setValueAtTime(0.012, audioCtx.currentTime);
        
        droneOsc.connect(droneGain);
        droneGain.connect(audioCtx.destination);
        
        droneOsc.start();
    } catch (e) {
        console.warn("Audio drone syntheziser initialization delayed until gesture.", e);
    }
}

function stopAmbientHum() {
    try {
        if (droneOsc) {
            droneOsc.stop();
            droneOsc.disconnect();
            droneOsc = null;
        }
        if (droneGain) {
            droneGain.disconnect();
            droneGain = null;
        }
    } catch (e) {
        console.warn("Audio drone stop call error:", e);
    }
}

