function updateQuizScenarioText() {
    const scenarioEl = document.getElementById("quizScenarioText");
    if (!scenarioEl) return;
    
    scenarioEl.setAttribute("data-key", quizScenarios[currentQuizIndex].qKey);
    scenarioEl.textContent = quizScenarios[currentQuizIndex].answers[currentLang].text;
    
    // Toggle visual components based on scenario type (Audio vs Scan image)
    const waveVisual = document.getElementById("visualWaveform");
    const scanVisual = document.getElementById("visualScan");
    
    if (quizScenarios[currentQuizIndex].type === 'audio') {
        waveVisual.classList.remove("hidden");
        scanVisual.classList.add("hidden");
    } else {
        waveVisual.classList.add("hidden");
        scanVisual.classList.remove("hidden");
    }
    
    updateDeepfakeInspector();
}

function updateDeepfakeInspector() {
    const featureBox = document.getElementById("detectorFeatures");
    if (!featureBox) return;
    
    const quiz = quizScenarios[currentQuizIndex];
    const isLikelyFake = quiz.fakeScore >= 50;
    const scoreText = currentLang === 'DE'
        ? `Fake-Score ${quiz.fakeScore}%`
        : `оценка фейка ${quiz.fakeScore}%`;
    const chips = quiz.signals[currentLang].map(signal => {
        return `<span class="feature-chip ${isLikelyFake ? 'fake' : 'real'}">${signal}</span>`;
    });
    featureBox.innerHTML = `<span class="feature-chip ${isLikelyFake ? 'fake' : 'real'}">${scoreText}</span>${chips.join("")}`;
}

function checkQuizAnswer(userSelectedReal) {
    const quiz = quizScenarios[currentQuizIndex];
    const isCorrect = (userSelectedReal === quiz.isReal);
    
    playSynthesizedSound(isCorrect ? 'correct' : 'error');
    
    // Disable quiz selection buttons
    document.querySelectorAll(".quiz-options .quiz-btn").forEach(btn => btn.setAttribute("disabled", "true"));
    
    // Show feedback
    const feedbackBox = document.getElementById("quizFeedback");
    const feedbackText = document.getElementById("quizFeedbackText");
    
    feedbackBox.classList.remove("hidden");
    
    let resultHeader = "";
    if (currentLang === 'DE') {
        resultHeader = isCorrect ? "✅ Richtig! " : "❌ Falsch! ";
    } else {
        resultHeader = isCorrect ? "✅ Правильно! " : "❌ Неверно! ";
    }
    
    const modelVerdict = currentLang === 'DE'
        ? `KI-Detektor: ${quiz.fakeScore}% Fake-Wahrscheinlichkeit.`
        : `ИИ-детектор: ${quiz.fakeScore}% вероятность подделки.`;
    feedbackText.innerHTML = `<strong>${resultHeader}</strong> ${quiz.answers[currentLang].explanation}<br><strong>${modelVerdict}</strong>`;
}

