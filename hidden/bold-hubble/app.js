function translateApp(lang) {
    currentLang = lang;
    
    // Find all elements with a 'data-key' attribute and replace their contents
    document.querySelectorAll("[data-key]").forEach(element => {
        const key = element.getAttribute("data-key");
        if (dictionary[key] && dictionary[key][lang]) {
            // Check if we need to preserve formatting (some contain strong tags)
            if (dictionary[key][lang].includes("**") || dictionary[key][lang].includes("<")) {
                element.innerHTML = dictionary[key][lang]
                    .replace(/\*\*(.*?)\*\*/g, "<strong>$1</strong>"); // Convert simple markdown bold
            } else {
                element.textContent = dictionary[key][lang];
            }
        }
    });

    // Update active state of language switcher buttons
    document.getElementById("langDE").classList.toggle("active", lang === 'DE');
    document.getElementById("langRU").classList.toggle("active", lang === 'RU');
    
    // Re-trigger active sector card text update so that it dynamically translates
    const activeSectorBtn = document.querySelector(".sector-btn.active");
    if (activeSectorBtn) {
        const onclickAttr = activeSectorBtn.getAttribute("onclick");
        const sector = onclickAttr.match(/'([^']+)'/)[1];
        updateSectorContent(sector);
    }
    
    // Re-translate current active eco activity
    const activeEcoBtn = document.querySelector(".activity-selector .sector-btn.active");
    if (activeEcoBtn) {
        const onclickAttr = activeEcoBtn.getAttribute("onclick");
        const act = onclickAttr.match(/'([^']+)'/)[1];
        updateEcoMetrics(act);
    }
    
    // Re-translate active quiz scenario
    updateQuizScenarioText();

    // Re-render glossary, speaker notes, and class quiz
    if (typeof populateGlossaryDrawer === 'function') populateGlossaryDrawer();
    if (typeof updateSpeakerNotesUI === 'function') updateSpeakerNotesUI();
    if (typeof renderClassQuizQuestion === 'function' && document.getElementById("classQuizQuestion")) {
        renderClassQuizQuestion();
    }
}

