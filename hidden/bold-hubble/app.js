function updateBiasExamples(balance) {
    const grid = document.getElementById("biasExampleRows");
    if (!grid) return;
    
    const unfairPenalty = Math.round((100 - balance) * 0.32);
    grid.innerHTML = biasApplicants.map(applicant => {
        const groupPenalty = applicant.group === "B" ? unfairPenalty : 0;
        const gapPenalty = applicant.gap ? Math.round(unfairPenalty * 0.45) : 0;
        const score = Math.max(0, applicant.skill - groupPenalty - gapPenalty);
        const accepted = score >= 72;
        const label = currentLang === 'DE'
            ? `${applicant.name}: Fähigkeit ${applicant.skill} → KI-Score ${score}`
            : `${applicant.name}: навык ${applicant.skill} → ИИ ${score}`;
        return `<span class="data-chip ${accepted ? 'ok' : 'warn'}">${label}</span>`;
    }).join("");
}

