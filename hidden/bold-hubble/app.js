/* ==========================================================================
   PHYSICALLY-DAMPENED MOUSE-GLOW SPRING CURSOR (FINE DEVICES)
   ========================================================================== */
let mouseX = 0;
let mouseY = 0;
let dotX = 0;
let dotY = 0;
let circleX = 0;
let circleY = 0;

function initSpringCursor() {
    window.addEventListener("mousemove", (e) => {
        mouseX = e.clientX;
        mouseY = e.clientY;
    });
    
    // Animate loop with spring coefficients
    animateCursor();
    
    // Add clickable hover detection rules for cyber dot cursor
    document.addEventListener("mouseover", (e) => {
        const target = e.target;
        if (!target) return;
        
        if (target.closest('a') || 
            target.closest('button') || 
            target.closest('[onclick]') || 
            target.closest('.toc-item') || 
            target.closest('.agenda-card') || 
            target.closest('.flip-card') || 
            target.closest('.glossary-term') ||
            target.closest('input[type="range"]')) {
            document.body.classList.add("clickable-hover");
        } else {
            document.body.classList.remove("clickable-hover");
        }
    });
}

function animateCursor() {
    // Fast tracking for the small central dot
    dotX += (mouseX - dotX) * 0.28;
    dotY += (mouseY - dotY) * 0.28;
    
    // Lagging spring physics for outer glow circle (0.09 spring dampening factor)
    circleX += (mouseX - circleX) * 0.09;
    circleY += (mouseY - circleY) * 0.09;
    
    const dotEl = document.getElementById("customCursorDot");
    const circleEl = document.getElementById("customCursorCircle");
    
    if (dotEl) {
        dotEl.style.left = `${dotX}px`;
        dotEl.style.top = `${dotY}px`;
    }
    
    if (circleEl) {
        circleEl.style.left = `${circleX}px`;
        circleEl.style.top = `${circleY}px`;
    }
    
    requestAnimationFrame(animateCursor);
}

